# Creating a dictionary
my_dict = {
    "name": "John",
    "age": 30,
    "city": "New York"
}

# Accessing values
print("Name:", my_dict["name"])  # Output: John
print("Age:", my_dict.get("age"))  # Output: 30

# Modifying values
my_dict["age"] = 31
print("Updated Age:", my_dict["age"])  # Output: 31

# Adding new key-value pairs
my_dict["occupation"] = "Engineer"
print("Occupation:", my_dict["occupation"])  # Output: Engineer

# Removing key-value pairs
del my_dict["city"]
print("Dictionary after removing 'city':", my_dict)  # Output: {'name': 'John', 'age': 31, 'occupation': 'Engineer'}

# Iterating over keys
print("Keys:")
for key in my_dict:
    print(key)

# Iterating over values
print("Values:")
for value in my_dict.values():
    print(value)

# Iterating over key-value pairs
print("Key-Value Pairs:")
for key, value in my_dict.items():
    print(key, ":", value)
